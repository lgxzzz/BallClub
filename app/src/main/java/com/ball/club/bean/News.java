package com.ball.club.bean;

public class News {
        String NEWS_ID;
        String NEWS_TYPE;
        String NEWS_CONTEX;
        String NEWS_URL;
        int NEWS_PIC_ID;

    public String getNEWS_ID() {
        return NEWS_ID;
    }

    public void setNEWS_ID(String NEWS_ID) {
        this.NEWS_ID = NEWS_ID;
    }

    public String getNEWS_TYPE() {
        return NEWS_TYPE;
    }

    public void setNEWS_TYPE(String NEWS_TYPE) {
        this.NEWS_TYPE = NEWS_TYPE;
    }

    public String getNEWS_CONTEX() {
        return NEWS_CONTEX;
    }

    public void setNEWS_CONTEX(String NEWS_CONTEX) {
        this.NEWS_CONTEX = NEWS_CONTEX;
    }

    public String getNEWS_URL() {
        return NEWS_URL;
    }

    public void setNEWS_URL(String NEWS_URL) {
        this.NEWS_URL = NEWS_URL;
    }

    public int getNEWS_PIC_ID() {
        return NEWS_PIC_ID;
    }

    public void setNEWS_PIC_ID(int NEWS_PIC_ID) {
        this.NEWS_PIC_ID = NEWS_PIC_ID;
    }
}
